def ranging_code(w, p):
    weil = np.logical_xor(
        legendre_10223,
        np.roll(legendre_10223, -w)
    )
    expansion = np.array([
        False, True, True, False, True, False, False
    ])
    return np.concatenate((
        weil[:p-1],
        expansion,
        weil[p-1:]
    ))


